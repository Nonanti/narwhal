#[test]
fn default_drop_and_set() {
    let with_default = {
        let mut c = col("c", "int4");
        c.default = Some("0".into());
        c
    };
    // SET DEFAULT path
    let d_set = diff(
        &[table("u", vec![with_default.clone()])],
        &[table("u", vec![col("c", "int4")])],
    );
    let sql_set = GenericEmitter::new().emit(&d_set).unwrap();
    assert!(sql_set.contains("SET DEFAULT 0"), "got:\n{sql_set}");

    // DROP DEFAULT path
    let d_drop = diff(
        &[table("u", vec![col("c", "int4")])],
        &[table("u", vec![with_default])],
    );
    let sql_drop = GenericEmitter::new().emit(&d_drop).unwrap();
    assert!(sql_drop.contains("DROP DEFAULT"), "got:\n{sql_drop}");
}

#[test]
fn empty_diff_emits_only_header() {
    let sql = GenericEmitter::new().emit(&SchemaDiff::default()).unwrap();
    assert!(sql.starts_with("-- generated by narwhal schema-diff"));
    // No DDL lines — only the header.
    let lines: Vec<&str> = sql.lines().filter(|l| !l.starts_with("--")).collect();
    assert!(lines.iter().all(|l| l.trim().is_empty()), "got:\n{sql}");
}

#[test]
fn type_change_emits_todo_comment() {
    let source = vec![table("u", vec![col("c", "int8")])];
    let target = vec![table("u", vec![col("c", "int4")])];
    let d = diff(&source, &target);
    let sql = GenericEmitter::new().emit(&d).unwrap();
    // The generic emitter cannot pick the right `USING` clause; it
    // emits the intent as a TODO comment so the operator sees it
    // instead of running a half-broken statement.
    assert!(
        sql.contains("-- TODO: change type of public.u.c"),
        "got:\n{sql}"
    );
}

#[test]
fn emitter_lookup_returns_generic_for_known_aliases() {
    assert!(emitter_by_name("generic").is_some());
    assert!(emitter_by_name("ANSI").is_some());
    assert!(emitter_by_name("sql").is_some());
    assert!(emitter_by_name("unknown-dialect").is_none());
}

