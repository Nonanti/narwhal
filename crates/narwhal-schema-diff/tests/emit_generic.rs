#[test]
fn nullable_drop_not_null() {
    let mut source_col = col("c", "int4");
    let target_col = {
        let mut c = col("c", "int4");
        c.nullable = false;
        c
    };
    source_col.nullable = true;
    let source = vec![table("u", vec![source_col])];
    let target = vec![table("u", vec![target_col])];
    let d = diff(&source, &target);
    let sql = GenericEmitter::new().emit(&d).unwrap();
    assert!(
        sql.contains("DROP NOT NULL"),
        "should drop NOT NULL when source is nullable; got:\n{sql}"
    );
}

